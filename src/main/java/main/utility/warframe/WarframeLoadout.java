package main.utility.warframe;

public class WarframeLoadout {
    private String id;
    private String name;
    private String type;
    private int masteryRank;
    private String image; // url
    private String wiki;

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getType() {
        return type;
    }

    public int getMasteryRank() {
        return masteryRank;
    }

    public String getImage() {
        return image;
    }

    public String getWiki() {
        return wiki;
    }
}
