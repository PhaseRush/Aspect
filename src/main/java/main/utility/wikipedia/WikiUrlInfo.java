package main.utility.wikipedia;

public class WikiUrlInfo {
    private String page;
    private String revisions;
    private String edit;
    private String talk;

    public String getPage() {
        return page;
    }

    public String getRevisions() {
        return revisions;
    }

    public String getEdit() {
        return edit;
    }

    public String getTalk() {
        return talk;
    }
}
