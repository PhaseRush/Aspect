public class WikiApiUrls {
    private String summary;
    private String metatdata;
    private String references;
    private String media;
    private String edit_html;
    private String talk_page_html;

    public String getSummary() {
        return summary;
    }

    public String getMetatdata() {
        return metatdata;
    }

    public String getReferences() {
        return references;
    }

    public String getMedia() {
        return media;
    }

    public String getEdit_html() {
        return edit_html;
    }

    public String getTalk_page_html() {
        return talk_page_html;
    }
}
